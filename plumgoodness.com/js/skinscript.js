var cart = [];
function storeItem(items) {
  var flag = false;
  cart.map(function (value) {
    if (items == value) {
      flag = true;
    }
  });
  if (!flag) {
    cart.push(items);
  } 

  localStorage.setItem("Usercart", JSON.stringify(cart));
}


